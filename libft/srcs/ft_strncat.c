#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*c1;

	c1 = s1;
	while (*c1)
		c1++;
	while (*s2 && n > 0)
	{
		*c1 = *s2;
		c1++;
		s2++;
		n--;
	}
	*c1 = '\0';
	return (s1);
}
